    private void ButtonCadastrarNovoProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonCadastrarNovoProdutoActionPerformed
        if (ComboTipo.getSelectedItem().equals("Óculos")) {
            ptxt.inserir(new oculos(CampoNomeNovoProduto.getText(), RadioSimNovoProduto.isSelected(), Integer.parseInt(CampoCodigoNovoProduto.getText()), Double.parseDouble(CampoValorNovoProduto.getText()), TextModelo.getText(), TextCor.getText(), Integer.parseInt(TextTamanho.getText())));
            TextModelo.setText("");
            TextCor.setText("");
            TextTamanho.setText("");
        } else if (ComboTipo.getSelectedItem().equals("Lente")) {
            ptxt.inserir(new lentes(CampoNomeNovoProduto.getText(), RadioSimNovoProduto.isSelected(), Integer.parseInt(CampoCodigoNovoProduto.getText()), Double.parseDouble(CampoValorNovoProduto.getText()), Double.parseDouble(TextGrauOlhoEsquerdoLente.getText()), Double.parseDouble(TextGrauOlhoDireitoLente.getText()), CheckAntiReflexo.isSelected(), CheckAntiRisco.isSelected(), CheckCrizal.isSelected(), CheckTransition.isSelected(), CheckVideoFilter.isSelected()));
            TextGrauOlhoEsquerdoLente.setText("");
            TextGrauOlhoDireitoLente.setText("");
            CheckAntiReflexo.setSelected(false);
            CheckAntiRisco.setSelected(false);
            CheckCrizal.setSelected(false);
            CheckTransition.setSelected(false);
            CheckVideoFilter.setSelected(false);
        } else if (ComboTipo.getSelectedItem().equals("LentesDeContato")) {
            ptxt.inserir(new lentesDeContato(CampoNomeNovoProduto.getText(), RadioSimNovoProduto.isSelected(), Integer.parseInt(CampoCodigoNovoProduto.getText()), Double.parseDouble(CampoValorNovoProduto.getText()), TextCorLentedeContato.getText(), Double.parseDouble(TextGrauOlhoEsquerdoLentedeContato.getText()), Double.parseDouble(TextGrauOlhoDireitoLentesDeContato.getText())));
            TextGrauOlhoDireitoLentesDeContato.setText("");
            TextGrauOlhoEsquerdoLentedeContato.setText("");
            TextCorLentedeContato.setText("");
        }
        CampoNomeNovoProduto.setText("");
        CampoCodigoNovoProduto.setText("");
        CampoValorNovoProduto.setText("");
        RadioSimNovoProduto.setSelected(false);
        RadioNaoNovoProduto.setSelected(false);

        JOptionPane.showMessageDialog(this, "Produto Cadastrado Com sucesso");
    }//GEN-LAST:event_ButtonCadastrarNovoProdutoActionPerformed

    private void ButtonBuscarEstoqueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonBuscarEstoqueActionPerformed
        if (CampoBuscarEstoque.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Entrada Inválida");
            CampoBuscarEstoque.setText("");
        } else if (RadialNomeEstoque.isSelected()) {
            modelTabelaEstoque.setNumRows(0);
            ArrayList<produto> produtos = ptxt.listar(CampoBuscarEstoque.getText());
            if (produtos.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nada Encontrado");
                CampoBuscarEstoque.setText("");
            }else{
            for (produto p : produtos) {
                modelTabelaEstoque.addRow(new Object[]{p.getNome(), true, p.getCodigo(), p.getValor(),});
            }
            }
        } else if (RadialCodigoEstoque.isSelected()) {
            modelTabelaEstoque.setNumRows(0);
            ArrayList<produto> produtos = ptxt.listar(Integer.parseInt(CampoBuscarEstoque.getText()));
            if (produtos.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nada Encontrado");
                CampoBuscarEstoque.setText("");
            }else{
            for (produto p : produtos) {
                modelTabelaEstoque.addRow(new Object[]{p.getNome(), true, p.getCodigo(), p.getValor(),});
            }
            }
        } else if (!RadialCodigoEstoque.isSelected() && !RadialNomeEstoque.isSelected()) {
            JOptionPane.showMessageDialog(this, "Selecione Uma Categoria");
        }
    }//GEN-LAST:event_ButtonBuscarEstoqueActionPerformed

