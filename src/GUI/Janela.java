    private void CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarActionPerformed
        if (ctxt.buscar(Integer.parseInt(CampoCPFCadastrar.getText())) != null) {
            JOptionPane.showMessageDialog(this, "CPF Já Cadastrado");
        }
        if (CampoCPFCadastrar.getText().equals("") || CampoNomeCadastrar.getText().equals("") || CampoTelefoneCadastrar.getText().equals("") || CampoEmailCadastrar.getText().equals("") || CampoDataCadastrar.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Campos não Preenchidos");
        } else {
            cliente c = new cliente(CampoNomeCadastrar.getText(), CampoEmailCadastrar.getText(), dataUtil.stringToDate(CampoDataCadastrar.getText()), CampoCPFCadastrar.getText(), Integer.parseInt(CampoTelefoneCadastrar.getText()));
            ctxt.inserir(c);
            JOptionPane.showMessageDialog(this, "Cliente Cadastrado Com Sucesso");
            CampoCPFCadastrar.setText("");
            CampoNomeCadastrar.setText("");
            CampoEmailCadastrar.setText("");
            CampoTelefoneCadastrar.setText("");
            CampoDataCadastrar.setText("");
        }

    }//GEN-LAST:event_CadastrarActionPerformed

    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        cliente c1 = ctxt.buscar(Integer.parseInt(CampoCPFBuscar.getText()));
        if (CampoCPFBuscar.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Entrada Inválida");
        } else if (c1 == null) {
            JOptionPane.showMessageDialog(this, "Cliente não Encontrado");
        } else {
            CampoNomeBusca.setText(c1.getNome());
            CampoEmailBusca.setText(c1.getEmail());
            CampoTelefoneBusca.setText(c1.getTelefone() + "");
            CampoCPFBusca.setText(c1.getCpf());
            CampoDataBusca.setText(c1.getDataDeNascimento().toLocaleString().substring(0, 10));
        }

    }//GEN-LAST:event_BuscarActionPerformed

