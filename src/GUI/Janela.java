    private void adicionarProdutoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarProdutoButtonActionPerformed
        if (clienteCombo.isEnabled()) {
            JOptionPane.showMessageDialog(this, "Por Favor, Selecione um Cliente");
        } else {
            produto p1 = ptxt.add(comboProduto.getSelectedItem().toString());

            modelItens.addRow(new Object[]{p1.getCodigo(), p1.getNome(), p1.isEmEstoque(), p1.getValor(), (int) quantidadeSpinner.getValue()});

            f = f + ((int) quantidadeSpinner.getValue() * p1.getValor());

            valorTotalLabelNumero.setText(String.valueOf(f));
        }
    }//GEN-LAST:event_adicionarProdutoButtonActionPerformed

    private void confirmarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarButtonActionPerformed
        ArrayList<itemVenda> temp = new ArrayList<>();
        Random r = new Random();

        for (int i = 0; i < modelItens.getRowCount(); i++) {
            int l1 = r.nextInt(999999999);
            produto p = new produto((String) modelItens.getValueAt(i, 1), (boolean) modelItens.getValueAt(i, 2), (int) modelItens.getValueAt(i, 0), (double) modelItens.getValueAt(i, 3));
            itemVenda iv = new itemVenda(p, (int) modelItens.getValueAt(i, 4), l1);
            temp.add(iv);
        }
        int l = r.nextInt(999999999);
        cliente c = ctxt.buscar((String) comboCliente.getSelectedItem());
        venda v = new venda(l, dataUtil.systemdate(), c);
        vtxt.inserir(v, temp);
        JOptionPane.showMessageDialog(this, "Venda completa ; Numero da Nota Fiscal: " + l);

        valorTotalLabelNumero.setText("");
        modelItens.setNumRows(0);
        clienteCombo.setEnabled(true);
        quantidadeSpinner.setValue(1);
    }//GEN-LAST:event_confirmarButtonActionPerformed

